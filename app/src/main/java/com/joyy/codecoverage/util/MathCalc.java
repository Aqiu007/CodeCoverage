package com.joyy.codecoverage.util;

public class MathCalc {
    public MathCalc() {
    }

    public static int add(int x, int y) {
        return x + y;
    }

    public static int multiply(int x, int y) {
        return x * y;
    }

    public static float divide(float x, float y) {
        return x / y;
    }
}
